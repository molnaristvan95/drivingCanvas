function driveMe() {
  var k = event.key,
      wh = event.which,
      hx = window.getComputedStyle(document.getElementById('houses')).getPropertyValue('left'),
      n = hx.substring(0, (hx.length-2)),
      int = parseInt(n, 10).toPrecision(4),
      hx2 = window.getComputedStyle(document.getElementById('housesCopy')).getPropertyValue('left'),
      n2 = hx2.substring(0, (hx2.length-2)),
      int2 = parseInt(n2, 10).toPrecision(4),
      speed = 3;
  if (k === 'ArrowRight') {
    var l1 = int - speed,
        l2 = int2 - speed;
    houses.style.left = l1 + 'px';
    housesCopy.style.left = l2 + 'px';
  }
  if (wh === 37 || k === 'ArrowLeft') {
    var l3 = int-(-speed),
        l4 = int2-(-speed);
    document.getElementById('houses').style.left = l3 + 'px';
    document.getElementById('housesCopy').style.left = l4 + 'px';
  }
  return;
}
window.addEventListener('keydown', driveMe);

